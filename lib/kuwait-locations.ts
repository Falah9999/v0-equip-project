// Helper function to get all locations as a flat array
export function getAllLocations(lang: "en" | "ar") {
  const allLocations: { id: string; name: string; governorate: string }[] = []

  Object.entries(kuwaitLocations).forEach(([governorateId, governorate]) => {
    governorate.areas.forEach((area) => {
      allLocations.push({
        id: area.id,
        name: area.name[lang],
        governorate: governorate.name[lang],
      })
    })
  })

  return allLocations
}

// Helper function to get locations grouped by governorate
export function getLocationsByGovernorate(lang: "en" | "ar") {
  return Object.entries(kuwaitLocations).map(([governorateId, governorate]) => ({
    id: governorateId,
    name: governorate.name[lang],
    areas: governorate.areas.map((area) => ({
      id: area.id,
      name: area.name[lang],
    })),
  }))
}
